if (phaseArg === 1) {
  // ---- Scout ----
  phase('Scout')
  const scout = await agent(`
You are the SCOUT agent for the VisitGPS agent-readiness audit.

Read ${AUDIT_DIR}/SPEC.md sections "Resolved open questions" and "Phase 1 — Wide scan" → "Inventory" for full context.

Your task:
1. Fetch ${BASE_URL}/sitemap.xml. Follow any sitemap index references to leaf sitemaps. Extract every URL matching /listing/{slug}/{id}/. Save the raw sitemap dump to ${AUDIT_DIR}/scout/sitemap-dump.json.
2. Crawl these category index pages and harvest /listing/ URLs visible on each, so we can label each listing with its primary category:
   - ${BASE_URL}/hotels/
   - ${BASE_URL}/restaurants/
   - ${BASE_URL}/things-to-do/  (and discover its sub-categories from in-page nav)
   - ${BASE_URL}/events/
   - Any other partner-listing roots discovered from top-nav
   Save raw per-category page dumps to ${AUDIT_DIR}/scout/by-category/{slug}.html (or .json) so labels are auditable.
3. For each unique listing id:
   - Fetch the /listing/{slug}/{id}/ page.
   - Extract: name, address, phone, partner_website (outbound link to the partner's own site), hours if listed.
   - Determine primary_category (the category index page where it first appeared) and all_categories (every index where it was seen).
   - Capture sitemap_lastmod if present.
4. Write the unified inventory to ${AUDIT_DIR}/scout/partner-directory-clean.json as an array of records matching the partner record schema. Write the raw harvest summary to ${AUDIT_DIR}/scout/partner-directory-raw.json.

Constraints:
- Respect robots.txt. Use curl with a clear User-Agent string: "AICV-Audit/2026Q2 (+https://aicoachellavalley.org)".
- 1-second courtesy delay between requests to visitgreaterpalmsprings.com.
- Cite sources in your output: every record should retain the listing_url so it's traceable.
- If a listing has no outbound partner_website, mark partner_website: null and still include it.

Use Bash/curl + Write for files. Report back the partner count, category breakdown, and any anomalies (broken listings, redirects, listings without category labels, etc.).
`.trim(), {
    label: 'scout:inventory',
    phase: 'Scout',
    schema: SCOUT_RESULT_SCHEMA,
  })

  if (!scout) throw new Error('Scout returned null — aborting.')
  log(`Scout: ${scout.partner_count} partners across ${Object.keys(scout.categories).length} categories`)
  log(`Categories: ${JSON.stringify(scout.categories)}`)
  if (scout.anomalies && scout.anomalies.length) {
    log(`Anomalies: ${scout.anomalies.length}`)
    scout.anomalies.slice(0, 5).forEach(a => log(`  - ${a}`))
  }

  // ---- Wide scan ----
  phase('Wide scan')

  const inventoryRead = await agent(`
Read ${AUDIT_DIR}/scout/partner-directory-clean.json and return the full array of partner records as { partners: [...] }.
`.trim(), {
    label: 'wide-scan:read-inventory',
    phase: 'Wide scan',
    schema: {
      type: 'object',
      required: ['partners'],
      properties: { partners: { type: 'array', items: PARTNER_RECORD_SCHEMA } },
    },
  })

  if (!inventoryRead) throw new Error('Failed to read inventory.')
  const partners = inventoryRead.partners
  const CHUNK_SIZE = 12
  const chunks = chunkArray(partners, CHUNK_SIZE)
  log(`Wide scan: ${partners.length} partners in ${chunks.length} chunks of up to ${CHUNK_SIZE}`)

  const chunkResults = await parallel(chunks.map((chunkPartners, i) => () =>
    agent(`
You are an AUDITOR agent running Phase 1 rubric checks for chunk ${i} of the VisitGPS audit.

Read ${AUDIT_DIR}/SPEC.md section "Phase 1 scoring rubric (0-8)" for the rubric.

You are responsible for these ${chunkPartners.length} partners (JSON):
${JSON.stringify(chunkPartners, null, 2)}

For each partner with a partner_website:
1. HEAD then GET the partner_website. Check 200, SSL valid, load time under 5s.
2. Parse the HTML for:
   - viewport meta tag (mobile_ready)
   - JSON-LD or microdata schema blocks of relevant types (schema_present)
   - og:title + og:description + og:image (og_metadata)
   - Links/anchors suggesting FAQ pages (faq_present): look for /faq, /questions, /help, or FAQ schema
3. Fetch {partner_website}/llms.txt — 200 means llms_txt_present: true.
4. NAP check: compare the partner's directory-listed name/address/phone against what appears on the website's homepage or contact page. nap_consistent if all three match (allow trivial formatting differences).
5. Content freshness: look at sitemap dates, blog/news dates, copyright year, JSON-LD dateModified. content_fresh if any signal is within the last 6 months relative to 2026-06.
6. Citation density: do a quick web search check (or fetch known directory pages) for presence on Google Business, Yelp, TripAdvisor, OpenTable, Resy, official tourism boards. citation_density: true if ≥3 platforms cite.
7. GBP linked: does the partner's site link to its Google Business Profile, or include verifiable GBP structured data?

For partners with no partner_website: all checks false, score 0, tier D.

Apply the rubric: 1 point per dimension passed (the 8 numbered rubric dimensions; llms_txt_present and gbp_linked are recorded but NOT scored). Total 0-8.

Tier:
- 7-8 → A
- 4-6 → B
- 1-3 → C
- 0 → D

Be courteous: 1s delay between hits to the same host. User-Agent: "AICV-Audit/2026Q2 (+https://aicoachellavalley.org)".

Return { chunk_index: ${i}, partners: [scored records...], errors: [...] }.
Also append your scored records to ${AUDIT_DIR}/auditor/chunks/chunk-${i}.json so partial progress is recoverable if the workflow aborts.
`.trim(), {
      label: `auditor:chunk-${i}`,
      phase: 'Wide scan',
      schema: WIDE_SCAN_CHUNK_SCHEMA,
    })
  ))

  const allScored = chunkResults.filter(Boolean).flatMap(r => r.partners)
  log(`Wide scan complete: ${allScored.length} partners scored (${chunkResults.filter(Boolean).length}/${chunks.length} chunks succeeded)`)

  // ---- Aggregate + sample ----
  phase('Sample selection')

  const sample = await agent(`
You are the SAMPLE-SELECTION agent for Phase 1 of the VisitGPS audit.

Read ${AUDIT_DIR}/SPEC.md section "Phase 2 — Deep scan" → "Sample construction" for the sample selection rules.

Inputs:
- ${allScored.length} scored partners are available in the chunk outputs at ${AUDIT_DIR}/auditor/chunks/chunk-*.json. Aggregate them.

Your task:
1. Merge all chunk-*.json files into a single ${AUDIT_DIR}/auditor/partners-wide-scan.json (array of scored partner records).
2. Compute aggregates:
   - Tier counts overall (A/B/C/D).
   - Pass rate per rubric dimension (across all partners with websites). Write to ${AUDIT_DIR}/auditor/rubric-results-per-dimension.json.
   - Tier counts per category. Write to ${AUDIT_DIR}/auditor/partners-by-tier.csv with columns: id, name, primary_category, score, tier, partner_website.
3. Sample selection — for each category:
   - Take top 3 from A/B (highest scores in that category).
   - Take middle 5 from B.
   - Take bottom 3 from B or top of C.
   - If category has fewer than 11 partners, take all.
   - If a category has only C and D, include the strongest 5 of any tier and add the category name to categories_with_weakness.
   Write the sample to ${AUDIT_DIR}/auditor/sample-for-deep-scan.json as { categories: [...], sample: [partner records with category, tier, score] }.

Return: { tier_counts, sample_count, sample_path, categories_with_weakness }.
`.trim(), {
    label: 'sample:bucket-and-select',
    phase: 'Sample selection',
    schema: SAMPLE_SCHEMA,
  })

  if (!sample) throw new Error('Sample selection returned null.')
  log(`Tier counts: A=${sample.tier_counts.A} B=${sample.tier_counts.B} C=${sample.tier_counts.C} D=${sample.tier_counts.D}`)
  log(`Sample: ${sample.sample_count} partners selected for Phase 2`)
  if (sample.categories_with_weakness && sample.categories_with_weakness.length) {
    log(`Categories with widespread weakness: ${sample.categories_with_weakness.join(', ')}`)
  }

  return {
    phase: 1,
    status: 'complete',
    scout,
    wide_scan: { total_scored: allScored.length, chunks: chunks.length },
    sample,
    next_step: 'Courier review of partners-wide-scan.json, partners-by-tier.csv, sample-for-deep-scan.json before running phase 2.',
  }
}

